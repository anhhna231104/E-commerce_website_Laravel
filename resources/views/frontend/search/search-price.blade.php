<script>
    $(document).ready(function () {
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
        $('a.add-to-cart').click(function (e) {
            e.preventDefault()
            var getIDProduct = $(this).closest('div.overlay-content').find('p.id-product').attr('id')
            var loginCk = "{{Auth::check()}}"
            if (loginCk == "") {
                alert('Please login to add this item to your cart')
            }
            else {
                let id_user = $(this).closest('div.product-overlay').find('p.id_user').text()
                $.ajax({
                    type: 'post',
                    url: `{{url('/add-to-cart')}}`,
                    data: {
                        id_product: getIDProduct,
                        id_user: id_user,
                        qty: 1
                    },
                    success: function (res) {
                        console.log(res.res);
                        alert(res.res)
                        $(document).trigger('cart-updated');
                    },
                    error: function (err) {
                        console.log(err.responseText);
                    }
                })
            }
            // alert(loginCk)


        })
    })
</script>
